package com.example.comparison;

public class ComparisonNotes {

    public static void main(String[] args) {

        /*
         * JPA (Java Persistence API)
         * --------------------------
         * - Specification
         * - Defines annotations such as @Entity, @Id, @Table
         * - Requires an implementation like Hibernate
         */

        /*
         * Hibernate
         * ----------
         * - ORM Framework
         * - Implements JPA
         * - Maps Java objects to database tables
         * - Generates SQL automatically
         * - Manages sessions and transactions
         */

        /*
         * Spring Data JPA
         * ----------------
         * - Spring Framework module
         * - Uses JPA implementations like Hibernate
         * - Provides built-in CRUD methods
         * - Reduces boilerplate code
         * - Uses repositories
         */

        System.out.println("Exercise completed successfully.");
    }

}